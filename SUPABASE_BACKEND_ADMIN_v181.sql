create or replace function public.p2u_admin_assign_role(
  p_email text,
  p_role text,
  p_active boolean default true
) returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $$
declare target_user auth.users%rowtype;
declare clean_role text := lower(trim(coalesce(p_role,'')));
declare before_row public.p2u_admin_roles%rowtype;
declare after_row public.p2u_admin_roles%rowtype;
begin
  if not public.p2u_has_admin_role(array['owner']) then
    raise exception 'Owner role required' using errcode='42501';
  end if;
  if clean_role not in ('owner','admin','moderator') then raise exception 'Invalid role'; end if;
  select * into target_user from auth.users where lower(email)=lower(trim(p_email)) limit 1;
  if target_user.id is null then raise exception 'No account found for that email'; end if;
  select * into before_row from public.p2u_admin_roles where user_id=target_user.id;
  if target_user.id = auth.uid() and (clean_role <> 'owner' or coalesce(p_active,true) = false) then
    raise exception 'You cannot demote or deactivate your own owner account from the browser console';
  end if;
  if before_row.role = 'owner' and before_row.active = true and (clean_role <> 'owner' or coalesce(p_active,true) = false)
     and (select count(*) from public.p2u_admin_roles where role='owner' and active=true) <= 1 then
    raise exception 'At least one active owner must remain';
  end if;
  insert into public.p2u_admin_roles(user_id,role,active,created_by,updated_by)
  values(target_user.id,clean_role,coalesce(p_active,true),auth.uid(),auth.uid())
  on conflict(user_id) do update set role=excluded.role,active=excluded.active,updated_at=now(),updated_by=auth.uid()
  returning * into after_row;
  perform public.p2u_write_admin_audit('role.assign','admin_role',target_user.id::text,to_jsonb(before_row),to_jsonb(after_row),jsonb_build_object('email',target_user.email));
  return jsonb_build_object('user_id',after_row.user_id,'email',target_user.email,'role',after_row.role,'active',after_row.active,'updated_at',after_row.updated_at);
end;
$$;
revoke all on function public.p2u_admin_assign_role(text,text,boolean) from public;
grant execute on function public.p2u_admin_assign_role(text,text,boolean) to authenticated;

-- Bootstrap the first owner. Replace the email before running this script.
do $$
declare owner_email text := 'YOUR_ACCOUNT_EMAIL@example.com';
declare owner_id uuid;
begin
  if owner_email = 'YOUR_ACCOUNT_EMAIL@example.com' then
    raise exception 'Replace YOUR_ACCOUNT_EMAIL@example.com with the signed-in Predict2U account email before running v181 setup.';
  end if;
  select id into owner_id from auth.users where lower(email)=lower(owner_email) limit 1;
  if owner_id is null then
    raise exception 'No existing Supabase Auth user found for %', owner_email;
  end if;
  insert into public.p2u_admin_roles(user_id,role,active,created_by,updated_by)
  values(owner_id,'owner',true,owner_id,owner_id)
  on conflict(user_id) do update set role='owner',active=true,updated_at=now(),updated_by=owner_id;
end $$;

commit;
