#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const HERE = __dirname;
const file = path.join(HERE, "data.js");
const raw = fs.readFileSync(file, "utf8");
const match = raw.match(/window\.MATCHES\s*=\s*(\[[\s\S]*\]);?\s*$/);
if (!match) throw new Error("Could not parse window.MATCHES from data.js");
const matches = JSON.parse(match[1]);

function optional(name, fn) {
  try { return fn(require(path.join(HERE, name))); }
  catch (e) { console.warn(`${name} skipped: ${e.message}`); return null; }
}

optional("calib.js", mod => {
  const result = mod.buildOddsCalib(matches);
  console.log(`Odds calibration: ${result.leagues} league ledger(s), ${result.attached} match attachment(s).`);
});
optional("team-profiles.js", mod => {
  const updated = mod.updateTeamProfiles(matches);
  const attached = mod.attachProfiles(matches);
  console.log(`Team profiles: ${updated.teams} team(s), ${attached.attached} side attachment(s).`);
});
optional("model-calibration.js", mod => {
  const result = mod.attachModelCalibration(matches, { writeLedger: true });
  console.log(`Model calibration: ${result.groups} group(s), ${result.attached} attachment(s).`);
});

fs.writeFileSync(file,
  `/* AUTO-GENERATED by fast all-games pipeline on ${new Date().toLocaleString()} — do not edit by hand. */\n\n` +
  `window.DATA_UPDATED = ${JSON.stringify(new Date().toISOString())};\n` +
  `window.MATCHES = ${JSON.stringify(matches, null, 2)};\n`,
  "utf8"
);

optional("engine-learning.js", mod => { const r = mod.runBuild(); console.log(`Engine learning refreshed (${r.attached || 0} attachment(s)).`); });
optional("model-governance.js", mod => { const r = mod.runBuild(); console.log(`Model governance refreshed (${r.attached || 0} attachment(s)).`); });
optional("consensus-report.js", mod => { const r = mod.runBuild(); console.log(`Consensus refreshed (${r.summary?.qualified || 0}/${r.summary?.fixtures || 0}).`); });
optional("context-report.js", mod => { const r = mod.runBuild(); console.log(`Match context refreshed (${r.summary?.high || 0} high-risk).`); });
