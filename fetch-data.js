// Build the list of date strings to fetch: today-back .. today+fwd (inclusive).
function buildDateWindow(back, fwd){
  const dates = [];
  const base = new Date();
  for (let off = -back; off <= fwd; off++) {
    const d = new Date(base.getFullYear(), base.getMonth(), base.getDate() + off);
    dates.push(fmtDate(d));
  }
  return dates;
}
