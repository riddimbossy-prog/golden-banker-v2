// Fetches every FINISHED match in a league this season and computes the REAL
// hit-rate of each market across the whole league (Section 2/4/5 of the spec).
// One fetch per league, cached for the whole run. Respects the spec's minimum
// sample (50 matches) — below that, returns null so the engine returns NO BET.
// Also assigns a league identity (Section 14) from the resulting rates.
const LEAGUE_TREND_CACHE = {};
// Tally of how the Trend engine's data source did this run — printed at the end
// so the Actions log shows, at a glance, whether Trend has data to work with.
const TREND_STATS = { okLeagues:new Set(), nullLeagues:new Set(), backfilledLeagues:new Set(), tierLeagues:new Set() };
const FINISHED_SET = new Set(["FT","AET","PEN","AWD","WO"]);
async function getLeagueTrends(leagueId, season, key, sleepMs, table, tableSize){
  if (leagueId==null || !season) return null;
  const ck = `${leagueId}|${season}`;
  if (ck in LEAGUE_TREND_CACHE) return LEAGUE_TREND_CACHE[ck];
  let result = null;
  try {
    // pull finished fixtures for the CURRENT season AND the PREVIOUS season, so
    // tier-patterns have real depth even in early/off-season when the current
    // season is too thin on its own. Games are tagged by season so we can track
    // how much of each pattern is current vs backfilled (honesty flag below).
    const prevSeason = String(parseInt(season,10) - 1);
    const fetchSeason = async (seasonId) => {
      let acc = [], page = 1, pages = 1;
      do {
        let r = null;
        // API-Football rejects &page on some single-page league queries ("The Page
        // field do not exist"), so only append it when actually paginating (page>1).
        const pageParam = page > 1 ? `&page=${page}` : "";
        for (let attempt = 0; attempt < 3; attempt++) {
          try { r = await apiGet(`/fixtures?league=${leagueId}&season=${seasonId}&status=FT-AET-PEN${pageParam}`, key); break; }
          catch (e) {
            if (String(e.message).includes("RATE_LIMIT") && attempt < 2) {
              await sleep(2000 * (attempt + 1)); // back off 2s, then 4s, then give up
              continue;
            }
            throw e; // non-rate-limit error, or out of retries -> bubble up
          }
        }
        if (r && Array.isArray(r.response)) acc = acc.concat(r.response);
        pages = (r && r.paging && r.paging.total) ? r.paging.total : 1;
        page++;
        if (page<=pages && sleepMs) await sleep(sleepMs);
      } while (page <= pages && page <= 6); // hard cap 6 pages per season
      return acc;
    };

    const curRaw = await fetchSeason(season);
    const keep = f => f && f.fixture && FINISHED_SET.has(String(f.fixture.status.short||"").toUpperCase())
      && f.goals && f.goals.home!=null && f.goals.away!=null;
    let curGames = curRaw.filter(keep).map(g => (g.__season = "current", g));

    // Only spend the extra API calls on the previous season if the current one
    // is thin. If current already has depth, don't bother backfilling.
    let prevGames = [];
    if (curGames.length < 50) {
      if (sleepMs) await sleep(sleepMs);
      const prevRaw = await fetchSeason(prevSeason);
      prevGames = prevRaw.filter(keep).map(g => (g.__season = "previous", g));
    }

    const games = curGames.concat(prevGames);
    const n = games.length;
    const nCurrent = curGames.length;
    // DIAGNOSTIC: show real counts so a null result is explainable, not mysterious.
    console.log(`    trends: league ${leagueId} season ${season} -> current=${nCurrent} prev=${prevGames.length} combined=${n} (need 30)`);
    if (n < 30) { LEAGUE_TREND_CACHE[ck] = null; return null; } // min COMBINED sample; per-bucket guards (n>=8/10/12) remain the real quality gate
    const smallSample = nCurrent < 30;              // current season alone is thin
    const backfilled = prevGames.length > 0;        // patterns lean on last season
    const currentShare = n ? Math.round((nCurrent/n)*100)/100 : 0;

    // tally every market the spec scans
    let over05=0,over15=0,over25=0,over35=0,under15=0,under25=0,under35=0,under45=0;
    let bttsYes=0,bttsNo=0, homeWin=0,awayWin=0,draw=0, homeScored=0,awayScored=0;
    let homeFTS=0,awayFTS=0, homeCS=0,awayCS=0, gib=0; // gib = goal in both halves
    let fh_over05=0, fh_under15=0, sh_over05=0, sh_over15=0, htSamp=0;
    for (const g of games){
      const h=g.goals.home, a=g.goals.away, tot=h+a;
      if(tot>=1)over05++; if(tot>=2)over15++; if(tot>=3)over25++; if(tot>=4)over35++;
      if(tot<=1)under15++; if(tot<=2)under25++; if(tot<=3)under35++; if(tot<=4)under45++;
      if(h>0&&a>0)bttsYes++; else bttsNo++;
      if(h>a)homeWin++; else if(a>h)awayWin++; else draw++;
      if(h>0)homeScored++; else homeFTS++;
      if(a>0)awayScored++; else awayFTS++;
      if(a===0)homeCS++; if(h===0)awayCS++;
      const ht=g.score&&g.score.halftime;
      if(ht&&ht.home!=null&&ht.away!=null){
        htSamp++;
        const h1=ht.home+ht.away, h2=tot-h1;
        if(h1>=1)fh_over05++; if(h1<=1)fh_under15++;
        if(h2>=1)sh_over05++; if(h2>=2)sh_over15++;
        if(h1>0&&h2>0)gib++;
      }
    }
    const R=(c,d)=> d>0 ? Math.round((c/d)*100)/100 : null;
    const rates = {
      "Over 0.5":R(over05,n),"Over 1.5":R(over15,n),"Over 2.5":R(over25,n),"Over 3.5":R(over35,n),
      "Under 1.5":R(under15,n),"Under 2.5":R(under25,n),"Under 3.5":R(under35,n),"Under 4.5":R(under45,n),
      "BTTS Yes":R(bttsYes,n),"BTTS No":R(bttsNo,n),
      "Home Win":R(homeWin,n),"Away Win":R(awayWin,n),"Draw":R(draw,n),
      "Home Team Over 0.5":R(homeScored,n),"Away Team Over 0.5":R(awayScored,n),
      "Home Team FTS":R(homeFTS,n),"Away Team FTS":R(awayFTS,n),
      "Home Clean Sheet":R(homeCS,n),"Away Clean Sheet":R(awayCS,n),
    };
    if(htSamp>=Math.min(30,n*0.5)){
      rates["First Half Over 0.5"]=R(fh_over05,htSamp);
      rates["First Half Under 1.5"]=R(fh_under15,htSamp);
      rates["Second Half Over 0.5"]=R(sh_over05,htSamp);
      rates["Second Half Over 1.5"]=R(sh_over15,htSamp);
      rates["Goal in Both Halves"]=R(gib,htSamp);
    }
    // rank markets ≥70% strongest-first, keep top 3 (Section 12/13)
    // ---- TIER-PAIRING PATTERNS (owner spec): what matches between teams of
    // specific table positions in THIS league historically produce.
    // Tiers: LDR (1st), TOP (2-4), MID, BOT (last 4). Keyed home-first so
    // "leader at home" patterns survive. Buckets need 6+ games to emit.
    let tierPatterns = null;
    if (table && tableSize) {
      const tierOf = pos => pos==null?null : pos===1?"LDR" : pos<=4?"TOP" : pos>tableSize-4?"BOT" : "MID";
      const B = {};
      // TIER buckets use CURRENT-season games ONLY: they're classified by the
      // current table, so last-season games (whose teams may sit elsewhere now,
      // or aren't in this table) would be mis-tiered. League-wide market rates
      // above safely use both seasons; tier-pairings stay current-only for
      // correctness and fill in as the season plays.
      for (const g of games){
        if (g.__season !== "current") continue;
        const hR=table[g.teams&&g.teams.home&&g.teams.home.id], aR=table[g.teams&&g.teams.away&&g.teams.away.id];
        const th=tierOf(hR&&hR.rank), ta=tierOf(aR&&aR.rank);
        if(!th||!ta) continue;
        const h=g.goals.home, a=g.goals.away, tot=h+a;
        const keys=[`${th}v${ta}`];
        if((th==="LDR"||th==="TOP")&&(ta==="LDR"||ta==="TOP")) keys.push("TOP4vTOP4");
        if(th==="LDR") keys.push("LDR_HOME");
        for(const k of keys){
          const b=B[k]=B[k]||{n:0,o15:0,o25:0,u25:0,u35:0,btts:0,homeWin:0,draw:0,awayWin:0,homeNoLoss:0};
          b.n++; if(tot>=2)b.o15++; if(tot>=3)b.o25++; if(tot<=2)b.u25++; if(tot<=3)b.u35++;
          if(h>0&&a>0)b.btts++; if(h>a)b.homeWin++; else if(h<a)b.awayWin++; else b.draw++;
          if(h>=a)b.homeNoLoss++;
        }
      }
      tierPatterns={};
      for(const [k,b] of Object.entries(B)){
        if(b.n<6) continue;
        const r2=x=>Math.round((x/b.n)*100)/100;
        tierPatterns[k]={ n:b.n, rates:{ "Over 1.5":r2(b.o15),"Over 2.5":r2(b.o25),"Under 2.5":r2(b.u25),"Under 3.5":r2(b.u35),
          "BTTS Yes":r2(b.btts),"BTTS No":r2(b.n-b.btts),"Home Win":r2(b.homeWin),"Away Win":r2(b.awayWin),
          "Draw":r2(b.draw),"Home No Loss":r2(b.homeNoLoss) } };
      }
      if(!Object.keys(tierPatterns).length) tierPatterns=null;
    }

    const ranked = Object.entries(rates)
      .filter(([,v])=>v!=null && v>=0.70)
      .sort((a,b)=>b[1]-a[1]);
    const top3 = ranked.slice(0,3).map(([m,v])=>({market:m,rate:v}));

    // league identity (Section 14) from headline rates
    const gpg = R(over25,n)!=null ? (games.reduce((s,g)=>s+g.goals.home+g.goals.away,0)/n) : null;
    const drawR = R(draw,n), homeWinR = R(homeWin,n);
    let identity="Balanced";
    if(gpg!=null){
      if(gpg>=3.0 && rates["Over 2.5"]>=0.55) identity="Goal-Friendly";
      else if(rates["Under 3.5"]!=null && rates["Under 3.5"]>=0.78) identity="Controlled";
      else if(rates["BTTS No"]!=null && rates["BTTS No"]>=0.55 && gpg<2.4) identity="Defensive";
      else if(homeWinR!=null && homeWinR>=0.50) identity="Home-Dominant";
      else if(drawR!=null && drawR>=0.32) identity="Volatile";
    }
    result = { leagueId, season, sample:n, sampleCurrent:nCurrent, currentShare, backfilled, smallSample, rates, top3, identity, tierPatterns, gpg:gpg!=null?Math.round(gpg*100)/100:null };
  } catch(e){ result = null; console.log(`    trends: league ${leagueId} THREW -> ${e.message}`); }
  LEAGUE_TREND_CACHE[ck] = result;
  return result;
}

