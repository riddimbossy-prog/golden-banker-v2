create table if not exists public.p2u_news_reports (
  id bigint generated by default as identity primary key,
  reporter_id uuid not null references auth.users(id) on delete cascade,
  article_id bigint null references public.p2u_news_articles(id) on delete cascade,
  comment_id bigint null references public.p2u_news_comments(id) on delete cascade,
  reason text not null check(reason in ('spam','abuse','misleading','copyright','privacy','other')),
