-- Replace news push trigger with richer targeting metadata and explanations.
create or replace function public.p2u_push_from_news_article()
returns trigger
language plpgsql
security definer
set search_path=public
as $$
declare search_text text;
begin
  search_text:=lower(concat_ws(' ',new.title,new.summary,new.source_name,new.region,new.league,new.club,new.player));
  if new.published=true and new.moderation_status='visible' and new.push_eligible=true then
    insert into public.p2u_push_jobs(category,title,body,url,audience,payload,scheduled_for)
    values(
      'news',
      case when new.category='transfer' then 'Transfer update' when new.breaking then 'Breaking football news' else 'Football news' end,
      left(new.title,240),
      'news.html?article='||new.id::text,
      jsonb_build_object('type','all'),
      jsonb_build_object(
        'article_id',new.id,
        'news_type',new.category,
        'source',new.source_name,
        'source_verified',new.source_verified,
        'breaking',new.breaking,
        'region',new.region,
        'league',new.league,
        'club',new.club,
        'player',new.player,
        'search_text',left(search_text,1200)
      ),
      now()
    );
  end if;
  return new;
end;
$$;

