-- Run after SUPABASE_FOOTBALL_NEWS_v189.sql.
-- Adds followed topics, Read Later, source verification, reporting, moderation,
-- personalized news push preferences and server-side spam safeguards.

begin;

alter table public.p2u_news_sources
  add column if not exists verified boolean not null default false,
  add column if not exists trust_note text not null default '';

update public.p2u_news_sources
set verified=true,
    trust_note=case
      when trust_note='' then 'Known publisher feed reviewed by Predict2U.'
      else trust_note
    end
where name in ('BBC Sport Football','The Guardian Football','Sky Sports Football','ESPN Soccer');

alter table public.p2u_news_articles
  add column if not exists source_verified boolean not null default false,
  add column if not exists canonical_key text not null default '',
  add column if not exists featured boolean not null default false,
  add column if not exists pinned boolean not null default false,
  add column if not exists moderation_status text not null default 'visible',
  add column if not exists moderation_reason text not null default '';

alter table public.p2u_news_articles drop constraint if exists p2u_news_articles_moderation_status_check;
alter table public.p2u_news_articles add constraint p2u_news_articles_moderation_status_check
check(moderation_status in ('visible','review','hidden'));

update public.p2u_news_articles a
set source_verified=coalesce(s.verified,false)
from public.p2u_news_sources s
where a.source_id=s.id and a.source_verified is distinct from coalesce(s.verified,false);

update public.p2u_news_articles
set canonical_key=left(
  regexp_replace(
    regexp_replace(lower(title),'[^a-z0-9 ]','','g'),
    '\m(the|a|an|to|for|of|in|on|at|with|and|or|from|after|before|latest|breaking|official|confirmed)\M',
    '',
    'g'
  ),
  180
)
where canonical_key='';

create index if not exists p2u_news_articles_canonical_idx on public.p2u_news_articles(canonical_key,published_at desc);
create index if not exists p2u_news_articles_featured_idx on public.p2u_news_articles(pinned desc,featured desc,published_at desc);
create index if not exists p2u_news_articles_moderation_idx on public.p2u_news_articles(moderation_status,published_at desc);

