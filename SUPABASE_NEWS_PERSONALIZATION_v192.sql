    quiet_enabled=excluded.quiet_enabled,quiet_start=excluded.quiet_start,quiet_end=excluded.quiet_end,
    timezone=excluded.timezone,timezone_offset_minutes=excluded.timezone_offset_minutes,
    favorite_league_names=excluded.favorite_league_names,favorite_engine_names=excluded.favorite_engine_names,
    followed_user_ids=excluded.followed_user_ids,updated_at=now()
