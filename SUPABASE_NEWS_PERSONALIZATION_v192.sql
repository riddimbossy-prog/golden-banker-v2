create or replace function public.p2u_news_toggle_follow(p_entity_type text,p_entity_value text)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare kind text:=lower(trim(coalesce(p_entity_type,'')));
declare value_clean text:=left(trim(regexp_replace(coalesce(p_entity_value,''),'\s+',' ','g')),100);
declare was_following boolean;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  if kind not in ('club','league','country','player','source','topic') then raise exception 'Unsupported follow type'; end if;
  if char_length(value_clean)<2 then raise exception 'Follow topic is too short'; end if;
  select exists(select 1 from public.p2u_news_follows where user_id=auth.uid() and entity_type=kind and normalized_value=lower(value_clean)) into was_following;
  if was_following then
    delete from public.p2u_news_follows where user_id=auth.uid() and entity_type=kind and normalized_value=lower(value_clean);
    return jsonb_build_object('following',false,'entity_type',kind,'entity_value',value_clean);
  end if;
  insert into public.p2u_news_follows(user_id,entity_type,entity_value)
  values(auth.uid(),kind,value_clean)
  on conflict do nothing;
  return jsonb_build_object('following',true,'entity_type',kind,'entity_value',value_clean);
end;
$$;
revoke all on function public.p2u_news_toggle_follow(text,text) from public;
grant execute on function public.p2u_news_toggle_follow(text,text) to authenticated;

create or replace function public.p2u_news_toggle_bookmark(p_article_id bigint)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare exists_now boolean;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  if not exists(select 1 from public.p2u_news_articles where id=p_article_id and published=true and moderation_status='visible') then raise exception 'News article not found'; end if;
  select exists(select 1 from public.p2u_news_bookmarks where user_id=auth.uid() and article_id=p_article_id) into exists_now;
  if exists_now then
    delete from public.p2u_news_bookmarks where user_id=auth.uid() and article_id=p_article_id;
    return jsonb_build_object('saved',false,'article_id',p_article_id);
  end if;
  insert into public.p2u_news_bookmarks(user_id,article_id) values(auth.uid(),p_article_id) on conflict do nothing;
  return jsonb_build_object('saved',true,'article_id',p_article_id);
end;
$$;
revoke all on function public.p2u_news_toggle_bookmark(bigint) from public;
grant execute on function public.p2u_news_toggle_bookmark(bigint) to authenticated;

create or replace function public.p2u_news_report(
  p_article_id bigint default null,
  p_comment_id bigint default null,
  p_reason text default 'other',
  p_details text default ''
)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare reason_clean text:=lower(trim(coalesce(p_reason,'other')));
declare details_clean text:=left(trim(regexp_replace(coalesce(p_details,''),'\s+',' ','g')),500);
declare row_id bigint;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  if ((p_article_id is not null)::int + (p_comment_id is not null)::int) <> 1 then raise exception 'Choose one report target'; end if;
  if reason_clean not in ('spam','abuse','misleading','copyright','privacy','other') then reason_clean:='other'; end if;
  if (select count(*) from public.p2u_news_reports where reporter_id=auth.uid() and created_at>now()-interval '1 hour')>=8 then raise exception 'Too many reports. Try again later.'; end if;
  if p_article_id is not null and not exists(select 1 from public.p2u_news_articles where id=p_article_id) then raise exception 'Article not found'; end if;
  if p_comment_id is not null and not exists(select 1 from public.p2u_news_comments where id=p_comment_id) then raise exception 'Comment not found'; end if;
  insert into public.p2u_news_reports(reporter_id,article_id,comment_id,reason,details)
  values(auth.uid(),p_article_id,p_comment_id,reason_clean,details_clean)
  returning id into row_id;
  return jsonb_build_object('ok',true,'report_id',row_id);
end;
$$;
revoke all on function public.p2u_news_report(bigint,bigint,text,text) from public;
grant execute on function public.p2u_news_report(bigint,bigint,text,text) to authenticated;

-- Replace comment posting with rate limits, duplicate prevention and basic spam checks.
create or replace function public.p2u_news_post_comment(p_article_id bigint,p_body text)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare clean_body text:=trim(regexp_replace(coalesce(p_body,''),'\s+',' ','g'));
declare handle_value text:='member';
declare row_out public.p2u_news_comments%rowtype;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  if char_length(clean_body)<2 or char_length(clean_body)>600 then raise exception 'Comment must be 2 to 600 characters'; end if;
  if not exists(select 1 from public.p2u_news_articles where id=p_article_id and published=true and moderation_status='visible') then raise exception 'News article not found'; end if;
  if (select count(*) from public.p2u_news_comments where user_id=auth.uid() and created_at>now()-interval '10 minutes')>=10 then raise exception 'Comment limit reached. Try again shortly.'; end if;
  if exists(select 1 from public.p2u_news_comments where user_id=auth.uid() and article_id=p_article_id and lower(body)=lower(clean_body) and created_at>now()-interval '1 hour') then raise exception 'Duplicate comment'; end if;
  if clean_body ~* '(https?://[^ ]+.*https?://|telegram|whatsapp me|guaranteed win|free money|dm me for|contact me on|(.)\1{11,})' then raise exception 'Comment looks like spam'; end if;
  select left(coalesce(nullif(handle,''),'member'),50) into handle_value from public.profiles where id=auth.uid();
  insert into public.p2u_news_comments(article_id,user_id,handle_snapshot,body)
  values(p_article_id,auth.uid(),coalesce(handle_value,'member'),clean_body)
  returning * into row_out;
  return to_jsonb(row_out);
end;
$$;
revoke all on function public.p2u_news_post_comment(bigint,text) from public;
grant execute on function public.p2u_news_post_comment(bigint,text) to authenticated;

create or replace function public.p2u_admin_news_action(
  p_article_id bigint,
  p_action text,
  p_value boolean default true,
  p_reason text default ''
)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare action_clean text:=lower(trim(coalesce(p_action,'')));
declare reason_clean text:=left(trim(coalesce(p_reason,'')),500);
declare role_value text;
declare row_out public.p2u_news_articles%rowtype;
begin
  select role into role_value from public.p2u_admin_roles where user_id=auth.uid() and active=true;
  if role_value not in ('owner','admin','moderator') then raise exception 'Admin role required' using errcode='42501'; end if;
  if action_clean='feature' then
    update public.p2u_news_articles set featured=p_value,updated_at=now() where id=p_article_id returning * into row_out;
  elsif action_clean='pin' then
    update public.p2u_news_articles set pinned=p_value,updated_at=now() where id=p_article_id returning * into row_out;
  elsif action_clean='breaking' then
    update public.p2u_news_articles set breaking=p_value,push_eligible=(push_eligible or p_value),updated_at=now() where id=p_article_id returning * into row_out;
  elsif action_clean='hide' then
    update public.p2u_news_articles set moderation_status=case when p_value then 'hidden' else 'visible' end,moderation_reason=reason_clean,published=not p_value,updated_at=now() where id=p_article_id returning * into row_out;
  elsif action_clean='review' then
    update public.p2u_news_articles set moderation_status=case when p_value then 'review' else 'visible' end,moderation_reason=reason_clean,updated_at=now() where id=p_article_id returning * into row_out;
  elsif action_clean='publish' then
    update public.p2u_news_articles set published=p_value,moderation_status=case when p_value then 'visible' else moderation_status end,updated_at=now() where id=p_article_id returning * into row_out;
  else
    raise exception 'Unsupported news action';
  end if;
  if row_out.id is null then raise exception 'Article not found'; end if;
  insert into public.p2u_admin_audit_log(actor_id,actor_role,action,target_type,target_key,metadata)
  values(auth.uid(),role_value,'news_'||action_clean,'news_article',p_article_id::text,jsonb_build_object('value',p_value,'reason',reason_clean));
  return to_jsonb(row_out);
end;
$$;
revoke all on function public.p2u_admin_news_action(bigint,text,boolean,text) from public;
grant execute on function public.p2u_admin_news_action(bigint,text,boolean,text) to authenticated;

create or replace function public.p2u_admin_news_resolve_report(
  p_report_id bigint,
  p_status text,
  p_hide_target boolean default false
)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare status_clean text:=lower(trim(coalesce(p_status,'')));
declare role_value text;
declare report_row public.p2u_news_reports%rowtype;
begin
  select role into role_value from public.p2u_admin_roles where user_id=auth.uid() and active=true;
  if role_value not in ('owner','admin','moderator') then raise exception 'Admin role required' using errcode='42501'; end if;
  if status_clean not in ('reviewing','actioned','dismissed') then raise exception 'Unsupported report status'; end if;
  select * into report_row from public.p2u_news_reports where id=p_report_id for update;
  if report_row.id is null then raise exception 'Report not found'; end if;
  update public.p2u_news_reports set status=status_clean,reviewed_at=now(),reviewed_by=auth.uid() where id=p_report_id;
  if p_hide_target and report_row.article_id is not null then
    update public.p2u_news_articles set published=false,moderation_status='hidden',moderation_reason='Hidden from report #'||p_report_id::text,updated_at=now() where id=report_row.article_id;
  elsif p_hide_target and report_row.comment_id is not null then
    update public.p2u_news_comments set status='hidden',updated_at=now() where id=report_row.comment_id;
  end if;
  insert into public.p2u_admin_audit_log(actor_id,actor_role,action,target_type,target_key,metadata)
  values(auth.uid(),role_value,'news_report_'||status_clean,'news_report',p_report_id::text,jsonb_build_object('hide_target',p_hide_target));
  return jsonb_build_object('ok',true,'report_id',p_report_id,'status',status_clean,'target_hidden',p_hide_target);
end;
$$;
revoke all on function public.p2u_admin_news_resolve_report(bigint,text,boolean) from public;
grant execute on function public.p2u_admin_news_resolve_report(bigint,text,boolean) to authenticated;

