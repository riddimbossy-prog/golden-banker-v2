#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = __dirname;
const shardRoot = path.resolve(process.argv[2] || "shards");
const discoveryPath = path.resolve(process.argv[3] || "discovery/all-games-discovery.json");

function walk(dir, filename, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, filename, out);
    else if (entry.isFile() && entry.name === filename) out.push(full);
  }
  return out;
}

function readMatches(file) {
  const raw = fs.readFileSync(file, "utf8");
  const match = raw.match(/window\.MATCHES\s*=\s*(\[[\s\S]*\]);?\s*$/);
  if (!match) throw new Error(`Could not parse window.MATCHES from ${file}`);
  return JSON.parse(match[1]);
}

function uniqueKey(item) {
  return item.id != null
    ? `id:${item.id}`
    : `match:${item.matchDate || ""}|${item.kickoff || ""}|${item.home || item.homeTeam || ""}|${item.away || item.awayTeam || ""}`;
}

function mergeCaches(files) {
  const merged = {};
  const now = Date.now();
  for (const file of files) {
    try {
      const cache = JSON.parse(fs.readFileSync(file, "utf8"));
      for (const [key, value] of Object.entries(cache || {})) {
        if (!value || Number(value.expiresAt || 0) <= now) continue;
        if (!merged[key] || Number(value.savedAt || 0) > Number(merged[key].savedAt || 0)) merged[key] = value;
      }
    } catch (e) {
      console.warn(`Cache skipped (${file}): ${e.message}`);
    }
  }
  const rows = Object.entries(merged)
    .sort((a, b) => Number(b[1].savedAt || 0) - Number(a[1].savedAt || 0))
    .slice(0, 20000);
  fs.writeFileSync(path.join(ROOT, "api-cache.json"), JSON.stringify(Object.fromEntries(rows), null, 2) + "\n");
}

const manifest = JSON.parse(fs.readFileSync(discoveryPath, "utf8"));
const windowDates = new Set(manifest.dates || []);
const shardFiles = walk(shardRoot, "data.js");
if (!shardFiles.length) throw new Error(`No shard data.js files found below ${shardRoot}`);

let existing = [];
const rootData = path.join(ROOT, "data.js");
if (fs.existsSync(rootData)) {
  try { existing = readMatches(rootData); } catch (e) { console.warn(e.message); }
}

const byKey = new Map();
for (const item of existing) {
  if (!windowDates.has(item.matchDate)) byKey.set(uniqueKey(item), item);
}
let shardWindowCount = 0;
for (const file of shardFiles) {
  const rows = readMatches(file);
  for (const item of rows) {
    if (!windowDates.has(item.matchDate)) continue;
    byKey.set(uniqueKey(item), item);
    shardWindowCount += 1;
  }
}

const merged = [...byKey.values()];
merged.sort((a, b) => {
  if ((a.matchDate || "") !== (b.matchDate || "")) return (b.matchDate || "").localeCompare(a.matchDate || "");
  return (a.kickoff || "").localeCompare(b.kickoff || "");
});

fs.writeFileSync(rootData,
  `/* AUTO-GENERATED by fast all-games merge on ${new Date().toLocaleString()} — do not edit by hand. */\n\n` +
  `window.DATA_UPDATED = ${JSON.stringify(new Date().toISOString())};\n` +
  `window.MATCHES = ${JSON.stringify(merged, null, 2)};\n`,
  "utf8"
);

mergeCaches(walk(shardRoot, "api-cache.json").concat(fs.existsSync(path.join(ROOT, "api-cache.json")) ? [path.join(ROOT, "api-cache.json")] : []));
fs.copyFileSync(discoveryPath, path.join(ROOT, "all-games-discovery.json"));

const inWindow = merged.filter(item => windowDates.has(item.matchDate)).length;
console.log(`Merged ${shardFiles.length} shard(s). ${inWindow} unique fixture(s) are in the active window; ${merged.length} total saved matches.`);
console.log(`Raw shard rows examined in the window: ${shardWindowCount}. Discovery expected: ${manifest.fixtureCount}.`);
