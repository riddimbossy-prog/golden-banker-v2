const fs=require('fs');
const path=require('path');
const root=__dirname;
const read=f=>fs.readFileSync(path.join(root,f),'utf8');
const slip=read('slip.js');
const sw=read('sw.js');
const checks=[];
const check=(name,pass)=>checks.push({name,pass:!!pass});
check('build version v212',read('BUILD_VERSION.txt').trim()==='v212');
check('service worker v212',sw.includes("const VERSION='v212';")&&sw.includes("predict2u-v212"));
check('slip module is precached',sw.includes("'./slip.js'"));
check('FAB is always rendered',slip.includes("fab.classList.add('show')"));
check('empty-count badge hides without hiding button',slip.includes("c.hidden=legs.length===0"));
check('pointer drag starts',slip.includes("fab.addEventListener('pointerdown'"));
check('pointer drag moves',slip.includes("fab.addEventListener('pointermove'"));
check('drag click is suppressed',slip.includes('suppressClickUntil'));
check('position persists',slip.includes('p2u_slip_fab_position_v1')&&slip.includes('localStorage.setItem(FAB_POS_KEY'));
check('position restores',slip.includes('requestAnimationFrame(restore)'));
check('button avoids mobile nav through tablet and Fold widths',slip.includes("--p2u-mobile-nav-height")&&slip.includes('navReserve')&&slip.includes("matchMedia('(max-width:1180px)')"));
check('button sits above mobile nav',slip.includes('z-index:10020!important'));
check('drawer sits above mobile nav',slip.includes('z-index:10030'));
check('touch drag does not scroll page',slip.includes('touch-action:none'));
check('drag remains accessible',slip.includes('Drag to move')&&slip.includes("aria-expanded"));
const failed=checks.filter(x=>!x.pass);
const report={build:'v212',generated_at:new Date().toISOString(),passed:checks.length-failed.length,failed:failed.length,viewports:[320,344,393,768,884,1180],checks};
fs.writeFileSync(path.join(root,'slip-fab-report-v212.json'),JSON.stringify(report,null,2));
console.log(JSON.stringify(report,null,2));
if(failed.length) process.exit(1);
