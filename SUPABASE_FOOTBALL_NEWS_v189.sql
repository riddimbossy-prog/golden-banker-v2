create or replace function public.p2u_save_push_preferences(p_payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare row_out public.p2u_push_preferences%rowtype;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  insert into public.p2u_push_preferences(
    user_id,enabled,board_updates,match_status,favorite_leagues,favorite_engines,community_wins,
    followed_users,announcements,verified_only,football_news,transfer_news,quiet_enabled,quiet_start,quiet_end,timezone,
    timezone_offset_minutes,favorite_league_names,favorite_engine_names,followed_user_ids,updated_at
  ) values (
    auth.uid(),coalesce((p_payload->>'enabled')::boolean,true),coalesce((p_payload->>'board_updates')::boolean,true),
    coalesce((p_payload->>'match_status')::boolean,true),coalesce((p_payload->>'favorite_leagues')::boolean,true),
    coalesce((p_payload->>'favorite_engines')::boolean,true),coalesce((p_payload->>'community_wins')::boolean,true),
    coalesce((p_payload->>'followed_users')::boolean,false),coalesce((p_payload->>'announcements')::boolean,true),
    coalesce((p_payload->>'verified_only')::boolean,false),coalesce((p_payload->>'football_news')::boolean,true),
    coalesce((p_payload->>'transfer_news')::boolean,true),coalesce((p_payload->>'quiet_enabled')::boolean,false),
    coalesce(nullif(p_payload->>'quiet_start','')::time,'22:00'::time),coalesce(nullif(p_payload->>'quiet_end','')::time,'07:00'::time),
    left(coalesce(nullif(p_payload->>'timezone',''),'UTC'),80),greatest(-840,least(840,coalesce((p_payload->>'timezone_offset_minutes')::integer,0))),
    coalesce((select array_agg(left(value,120)) from (select value from jsonb_array_elements_text(coalesce(p_payload->'favorite_league_names','[]'::jsonb)) limit 80)x),'{}'::text[]),
    coalesce((select array_agg(left(value,80)) from (select value from jsonb_array_elements_text(coalesce(p_payload->'favorite_engine_names','[]'::jsonb)) limit 40)x),'{}'::text[]),
    coalesce((select array_agg(value::uuid) from (select value from jsonb_array_elements_text(coalesce(p_payload->'followed_user_ids','[]'::jsonb)) where value~*'^[0-9a-f-]{36}$' limit 500)x),'{}'::uuid[]),now()
  )
  on conflict(user_id) do update set
    enabled=excluded.enabled,board_updates=excluded.board_updates,match_status=excluded.match_status,
    favorite_leagues=excluded.favorite_leagues,favorite_engines=excluded.favorite_engines,community_wins=excluded.community_wins,
    followed_users=excluded.followed_users,announcements=excluded.announcements,verified_only=excluded.verified_only,
    football_news=excluded.football_news,transfer_news=excluded.transfer_news,quiet_enabled=excluded.quiet_enabled,
    quiet_start=excluded.quiet_start,quiet_end=excluded.quiet_end,timezone=excluded.timezone,
    timezone_offset_minutes=excluded.timezone_offset_minutes,favorite_league_names=excluded.favorite_league_names,
    favorite_engine_names=excluded.favorite_engine_names,followed_user_ids=excluded.followed_user_ids,updated_at=now()
  returning * into row_out;
  return to_jsonb(row_out);
end;
$$;
revoke all on function public.p2u_save_push_preferences(jsonb) from public;
grant execute on function public.p2u_save_push_preferences(jsonb) to authenticated;

-- Transfer and breaking stories create background push jobs. General stories stay
-- in the News tab and in-app feed to avoid excessive notification volume.
create or replace function public.p2u_push_from_news_article()
returns trigger
language plpgsql
security definer
set search_path=public
as $$
begin
  if new.published=true and new.push_eligible=true then
    insert into public.p2u_push_jobs(category,title,body,url,audience,payload,scheduled_for)
    values(
      'news',
      case when new.category='transfer' then 'Transfer update' when new.breaking then 'Breaking football news' else 'Football news' end,
      left(new.title,240),
      'news.html?article='||new.id::text,
      jsonb_build_object('type','all'),
      jsonb_build_object('article_id',new.id,'news_type',new.category,'source',new.source_name,'breaking',new.breaking),
      now()
    );
  end if;
  return new;
end;
$$;
drop trigger if exists p2u_news_push_trigger on public.p2u_news_articles;
create trigger p2u_news_push_trigger
after insert on public.p2u_news_articles
for each row execute function public.p2u_push_from_news_article();

