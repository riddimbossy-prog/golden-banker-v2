create table if not exists public.p2u_news_comments (
  id bigint generated by default as identity primary key,
  article_id bigint not null references public.p2u_news_articles(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  handle_snapshot text not null default 'member',
  body text not null check(char_length(body) between 1 and 600),
  status text not null default 'visible' check(status in ('visible','hidden','review')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create index if not exists p2u_news_comments_article_idx on public.p2u_news_comments(article_id,created_at);
create index if not exists p2u_news_comments_user_idx on public.p2u_news_comments(user_id,created_at desc);

alter table public.p2u_news_comments enable row level security;
drop policy if exists "Public reads visible news comments" on public.p2u_news_comments;
create policy "Public reads visible news comments" on public.p2u_news_comments
for select to anon,authenticated
using(status='visible' or user_id=auth.uid() or public.p2u_has_admin_role(array['owner','admin','moderator']));
drop policy if exists "Users delete own news comments" on public.p2u_news_comments;
create policy "Users delete own news comments" on public.p2u_news_comments
for delete to authenticated using(user_id=auth.uid());
drop policy if exists "Moderators manage news comments" on public.p2u_news_comments;
create policy "Moderators manage news comments" on public.p2u_news_comments
for update to authenticated
using(public.p2u_has_admin_role(array['owner','admin','moderator']))
with check(public.p2u_has_admin_role(array['owner','admin','moderator']));

create or replace function public.p2u_news_post_comment(p_article_id bigint,p_body text)
returns jsonb
language plpgsql
security definer
set search_path=public,auth
as $$
declare clean_body text:=trim(regexp_replace(coalesce(p_body,''),'\s+',' ','g'));
declare handle_value text:='member';
declare row_out public.p2u_news_comments%rowtype;
begin
  if auth.uid() is null then raise exception 'Sign in required' using errcode='42501'; end if;
  if char_length(clean_body)<1 or char_length(clean_body)>600 then raise exception 'Comment must be 1 to 600 characters'; end if;
  if not exists(select 1 from public.p2u_news_articles where id=p_article_id and published=true) then raise exception 'News article not found'; end if;
  select left(coalesce(nullif(handle,''),'member'),50) into handle_value from public.profiles where id=auth.uid();
  insert into public.p2u_news_comments(article_id,user_id,handle_snapshot,body)
  values(p_article_id,auth.uid(),coalesce(handle_value,'member'),clean_body)
  returning * into row_out;
  return to_jsonb(row_out);
end;
$$;
revoke all on function public.p2u_news_post_comment(bigint,text) from public;
grant execute on function public.p2u_news_post_comment(bigint,text) to authenticated;

create or replace function public.p2u_news_update_comment_count()
returns trigger
language plpgsql
security definer
set search_path=public
as $$
declare affected_id bigint;
begin
  affected_id:=case when tg_op='DELETE' then old.article_id else new.article_id end;
  update public.p2u_news_articles
  set comment_count=(select count(*) from public.p2u_news_comments where article_id=affected_id and status='visible'),updated_at=now()
  where id=affected_id;
  if tg_op='DELETE' then return old; end if;
  return new;
end;
$$;
drop trigger if exists p2u_news_comment_count_trigger on public.p2u_news_comments;
create trigger p2u_news_comment_count_trigger
after insert or update or delete on public.p2u_news_comments
for each row execute function public.p2u_news_update_comment_count();

